export function monsterLabel(type) {
  if (!type) return 'Monstro';
  const known = MONSTER_LABELS[type];
  if (known) return known;
  // Fallback legível: id com underscore → palavras
  return String(type)
    .split('_')
    .filter(Boolean)
    .join(' ');
}
