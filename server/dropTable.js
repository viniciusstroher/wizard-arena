/**
 * Resolve o tipo base usado na tabela de drops para um monstro,
 * removendo sufixos "_algo" do fim até achar uma entrada conhecida.
 */
function resolveDropType(monsterType) {
  if (DROP_TABLE[monsterType]) return monsterType;
  const parts = monsterType.split('_');
  while (parts.length > 1) {
    parts.pop();
    const base = parts.join('_');
    if (DROP_TABLE[base]) return base;
  }
  return null;
}

